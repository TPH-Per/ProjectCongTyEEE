// Helper function để tạo ID tự động
let _itemCounter = 0;
const genId = (prefix: string) => `${prefix}-${++_itemCounter}`;

// Helper format giá
const fmt = (price: number): string => {
  if (price === 0) return "0K";
  if (price >= 1000000)
    return `${(price / 1000000).toFixed(3).replace(/\.?0+$/, "")}M`;
  return `${(price / 1000).toFixed(0)}K`;
};

